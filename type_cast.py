# Converting to int
num_str = "10"
num_int = int(num_str)
print(num_int)  # Output: 10

# Converting to float
num_float = float(num_int)
print(num_float)  # Output: 10.0

# Converting to string
num_str2 = str(num_float)
print(num_str2)  # Output: "10.0"

# Converting to boolean
bool_val = bool(num_int)
print(bool_val)  # Output: True, since num_int is not zero
